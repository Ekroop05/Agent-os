"""
Evaluation Engine — autonomous quality scoring, gap detection, and improvement.

After every build cycle, this engine:
  1. Scores the project across multiple quality dimensions (0–100)
  2. Detects gaps between what was requested (spec) and what exists
  3. Generates improvement tasks to close those gaps

Maximum 3 improvement cycles to prevent infinite loops.
"""

from __future__ import annotations

import json
import logging
import os
import re
from typing import Any

from app.schemas import TaskCreate

logger = logging.getLogger("evaluation_engine")

# ── Dimension weights (must sum to 100) ───────────────────────────────────

DIMENSION_WEIGHTS = {
    "feature_coverage": 35,
    "structure_quality": 20,
    "code_quality": 15,
    "component_count": 15,
    "build_readiness": 10,
    "branding_check": 5,
}

# ── Branding strings that must NOT appear in user-facing files ────────────

_BANNED_BRANDING = [
    "built by agent os",
    "scaffolded by agent os",
    "generated by agent os",
    "built with agent os",
    "agent os builder",
    "agent os — local multi-agent runtime",
]

# ── Placeholder content that indicates unfinished work ────────────────────

_PLACEHOLDER_PATTERNS = [
    r"lorem ipsum",
    r"placeholder\s+text",
    r"todo:\s*implement",
    r"TODO:\s*add",
    r"this app was scaffolded",
    r"edit.*src/App\.jsx.*to start building",
    r"getting started",
    r"hello world",
    r"sample text here",
]


class EvaluationEngine:
    """Evaluates project quality and generates improvement tasks."""

    def evaluate(self, workspace_path: str, spec: dict) -> dict:
        """Run a full evaluation of the project against its spec.

        Returns an evaluation result dict with:
          - overall_score (0–100)
          - dimension_scores (dict)
          - missing_features (list)
          - gaps (list of gap dicts)
          - passed (bool)
        """
        native_path = workspace_path.replace("/", os.sep)
        quality_target = spec.get("quality_target", 85)

        # ── Score each dimension ──────────────────────────────────────
        scores = {
            "feature_coverage": self._score_feature_coverage(native_path, spec),
            "structure_quality": self._score_structure(native_path, spec),
            "code_quality": self._score_code_quality(native_path),
            "component_count": self._score_components(native_path),
            "build_readiness": self._score_build_readiness(native_path),
            "branding_check": self._score_branding(native_path),
        }

        # ── Weighted overall score ────────────────────────────────────
        overall = 0
        for dim, weight in DIMENSION_WEIGHTS.items():
            overall += scores.get(dim, 0) * (weight / 100)
        overall = int(min(100, max(0, overall)))

        # ── Detect gaps ───────────────────────────────────────────────
        missing_features = self._find_missing_features(native_path, spec)
        gaps = self._detect_all_gaps(native_path, spec, scores, missing_features)

        return {
            "overall_score": overall,
            "dimension_scores": scores,
            "missing_features": missing_features,
            "gaps": gaps,
            "quality_target": quality_target,
            "passed": overall >= quality_target and len(missing_features) == 0,
        }

    def detect_gaps(self, spec: dict, evaluation: dict) -> list[dict]:
        """Extract actionable gaps from an evaluation result."""
        return evaluation.get("gaps", [])

    def generate_improvement_tasks(
        self, gaps: list[dict], spec: dict, workspace_id: str
    ) -> list[TaskCreate]:
        """Convert detected gaps into concrete, spec-aware improvement tasks."""
        from app.services.spec_engine import spec_engine

        tasks: list[TaskCreate] = []
        seen_titles: set[str] = set()

        for gap in gaps:
            gap_type = gap.get("type", "unknown")
            title = ""
            description = ""

            if gap_type == "missing_feature":
                feature = gap.get("feature", "Unknown Feature")
                title = f"Implement {feature}"
                description = (
                    f"The project is missing the required feature: {feature}. "
                    f"Create the necessary components, pages, and/or styles to implement this feature "
                    f"according to the project specification."
                )

            elif gap_type == "placeholder_content":
                file_name = gap.get("file", "unknown file")
                title = f"Replace placeholder content in {file_name}"
                description = (
                    f"The file {file_name} contains placeholder or generic content. "
                    f"Replace it with real, theme-appropriate content that matches the project specification. "
                    f"Detail: {gap.get('detail', 'Generic content detected')}"
                )

            elif gap_type == "missing_structure":
                detail = gap.get("detail", "Missing project structure")
                title = f"Fix project structure: {detail}"
                description = f"The project structure is incomplete. {detail}"

            elif gap_type == "branding_violation":
                file_name = gap.get("file", "unknown file")
                title = f"Remove Agent OS branding from {file_name}"
                description = (
                    f"The file {file_name} contains Agent OS branding that should not appear "
                    f"in user-facing project files. Remove or replace the branded text."
                )

            elif gap_type == "low_component_count":
                title = "Add more UI components"
                description = (
                    f"The project has too few components ({gap.get('count', 0)}). "
                    f"Add additional React components to create a richer user interface. "
                    f"Consider adding components for: {', '.join(spec.get('required_features', [])[:5])}"
                )

            elif gap_type == "low_page_count":
                title = "Add more pages"
                description = (
                    f"The project has too few pages ({gap.get('count', 0)}). "
                    f"Add additional pages to provide a complete user experience."
                )

            elif gap_type == "broken_import":
                title = f"Fix broken import in {gap.get('file', 'unknown')}"
                description = f"Broken import detected: {gap.get('detail', 'Unknown import issue')}"

            else:
                title = f"Fix quality issue: {gap.get('detail', gap_type)}"
                description = f"Quality gap detected: {gap.get('detail', 'See gap details')}"

            # Deduplicate
            if title in seen_titles:
                continue
            seen_titles.add(title)

            # Enrich with spec context
            enriched_desc = spec_engine.enrich_task_description(title, description, spec)

            tasks.append(TaskCreate(
                title=title,
                description=enriched_desc,
                assigned_agent="Builder Agent",
                priority="High",
                workspace_id=workspace_id,
            ))

        return tasks

    # ── Dimension Scorers ─────────────────────────────────────────────────

    def _score_feature_coverage(self, project_path: str, spec: dict) -> int:
        """Score how many required features are present in the codebase."""
        required = spec.get("required_features", [])
        if not required:
            return 100

        found = 0
        for feature in required:
            if self._feature_exists(project_path, feature):
                found += 1

        return int((found / len(required)) * 100) if required else 100

    def _score_structure(self, project_path: str, spec: dict) -> int:
        """Score the project directory structure."""
        score = 100
        checks = [
            ("frontend", -15),
            ("frontend/src", -15),
            ("frontend/package.json", -10),
            ("frontend/index.html", -10),
            (".agentos", -5),
        ]

        has_backend = spec.get("backend") and spec["backend"] is not False
        if has_backend:
            checks.append(("backend", -15))
            checks.append(("backend/main.py", -10))

        for path_suffix, penalty in checks:
            full = os.path.join(project_path, path_suffix.replace("/", os.sep))
            if not os.path.exists(full):
                score += penalty  # penalty is negative

        return max(0, score)

    def _score_code_quality(self, project_path: str) -> int:
        """Score code quality — penalize empty files, placeholders, etc."""
        score = 100
        penalties = 0
        files_checked = 0

        src_dir = os.path.join(project_path, "frontend", "src")
        if not os.path.exists(src_dir):
            return 50  # No source dir is a significant problem

        for root, _, files in os.walk(src_dir):
            for fname in files:
                if not fname.endswith((".js", ".jsx", ".ts", ".tsx", ".css")):
                    continue
                files_checked += 1
                fpath = os.path.join(root, fname)
                try:
                    with open(fpath, "r", encoding="utf-8") as f:
                        content = f.read()

                    if not content.strip():
                        penalties += 10
                        continue

                    # Check for placeholder content
                    for pattern in _PLACEHOLDER_PATTERNS:
                        if re.search(pattern, content, re.IGNORECASE):
                            penalties += 5
                            break
                except (OSError, UnicodeDecodeError):
                    penalties += 2

        if files_checked > 0:
            score = max(0, 100 - penalties)

        return score

    def _score_components(self, project_path: str) -> int:
        """Score based on component count — more components = richer UI."""
        components = self._count_items(project_path, "components")
        pages = self._count_items(project_path, "pages")

        # Scoring: 5+ components and 2+ pages = 100
        comp_score = min(100, (components / 5) * 100) if components > 0 else 0
        page_score = min(100, (pages / 2) * 100) if pages > 0 else 0

        return int(comp_score * 0.6 + page_score * 0.4)

    def _score_build_readiness(self, project_path: str) -> int:
        """Score whether the project can actually build."""
        score = 100

        # Check package.json exists and is valid
        pkg = os.path.join(project_path, "frontend", "package.json")
        if os.path.exists(pkg):
            try:
                with open(pkg, "r", encoding="utf-8") as f:
                    data = json.load(f)
                if "dependencies" not in data and "devDependencies" not in data:
                    score -= 15
            except (json.JSONDecodeError, OSError):
                score -= 20
        else:
            score -= 30

        # Check for main entry files
        entry_files = ["frontend/src/main.jsx", "frontend/src/App.jsx", "frontend/index.html"]
        for entry in entry_files:
            full = os.path.join(project_path, entry.replace("/", os.sep))
            if not os.path.exists(full):
                score -= 15

        # Check for broken relative imports
        broken = self._find_broken_imports(project_path)
        score -= len(broken) * 5

        return max(0, score)

    def _score_branding(self, project_path: str) -> int:
        """Score: 100 if no Agent OS branding in user-facing files, 0 if found."""
        violations = self._find_branding_violations(project_path)
        if not violations:
            return 100
        # Each violation costs 20 points
        return max(0, 100 - len(violations) * 20)

    # ── Gap Detection ─────────────────────────────────────────────────────

    def _detect_all_gaps(
        self, project_path: str, spec: dict, scores: dict, missing_features: list[str]
    ) -> list[dict]:
        """Aggregate all detected gaps into a single list."""
        gaps: list[dict] = []

        # Missing features
        for feature in missing_features:
            gaps.append({
                "type": "missing_feature",
                "feature": feature,
                "suggestion": f"Create component(s) implementing {feature}",
            })

        # Placeholder content
        placeholders = self._find_placeholders(project_path)
        for ph in placeholders:
            gaps.append({
                "type": "placeholder_content",
                "file": ph["file"],
                "detail": ph["detail"],
            })

        # Branding violations
        violations = self._find_branding_violations(project_path)
        for v in violations:
            gaps.append({
                "type": "branding_violation",
                "file": v["file"],
                "detail": v["text"],
            })

        # Low component count
        comp_count = self._count_items(project_path, "components")
        if comp_count < 3:
            gaps.append({
                "type": "low_component_count",
                "count": comp_count,
                "detail": f"Only {comp_count} components found, need at least 3",
            })

        # Low page count
        page_count = self._count_items(project_path, "pages")
        if page_count < 2:
            gaps.append({
                "type": "low_page_count",
                "count": page_count,
                "detail": f"Only {page_count} pages found, need at least 2",
            })

        # Broken imports
        broken = self._find_broken_imports(project_path)
        for b in broken:
            gaps.append({
                "type": "broken_import",
                "file": b["file"],
                "detail": b["import"],
            })

        # Structure issues
        if scores.get("structure_quality", 100) < 70:
            gaps.append({
                "type": "missing_structure",
                "detail": "Project directory structure is incomplete",
            })

        return gaps

    # ── Feature Detection ─────────────────────────────────────────────────

    def _feature_exists(self, project_path: str, feature: str) -> bool:
        """Check if a feature likely exists in the codebase via keyword analysis."""
        # Map features to code-level keywords we'd expect to find
        feature_keywords: dict[str, list[str]] = {
            "Hero Section": ["hero", "Hero", "HeroBanner", "HeroSection", "hero-section"],
            "Character Showcase": ["character", "Character", "CharacterCard", "character-card", "roster"],
            "Navigation": ["nav", "Nav", "Navbar", "Navigation", "navbar", "navigation", "Header"],
            "Footer": ["footer", "Footer", "FooterSection"],
            "Responsive Layout": ["responsive", "@media", "max-width", "min-width", "flex", "grid"],
            "About Section": ["about", "About", "AboutSection", "about-section"],
            "Contact Section": ["contact", "Contact", "ContactForm", "contact-form"],
            "Contact Form": ["contact", "Contact", "ContactForm", "contact-form", "form"],
            "Dashboard": ["dashboard", "Dashboard", "DashboardPage"],
            "User Interface": ["interface", "ui", "UI"],
            "Image Gallery": ["gallery", "Gallery", "GalleryGrid"],
            "Search & Filter": ["search", "Search", "filter", "Filter", "SearchBar"],
            "User Authentication": ["login", "Login", "auth", "Auth", "SignIn"],
            "User Profiles": ["profile", "Profile", "UserProfile"],
            "Projects Showcase": ["project", "Project", "ProjectCard", "portfolio"],
            "Skills Section": ["skill", "Skill", "SkillsSection"],
            "About Me": ["about", "About", "AboutMe"],
            "Menu Section": ["menu", "Menu", "MenuSection", "MenuItem"],
            "Product Catalog": ["product", "Product", "catalog", "Catalog"],
            "Article List": ["article", "Article", "post", "Post", "BlogList"],
            "Features Section": ["feature", "Feature", "FeatureCard"],
            "Call to Action": ["cta", "CTA", "CallToAction"],
            "Testimonials": ["testimonial", "Testimonial", "review", "Review"],
            "Data Display": ["data", "table", "Table", "DataTable"],
            "Settings": ["settings", "Settings", "SettingsPage"],
            "Metrics Overview": ["metric", "Metric", "stats", "Stats"],
        }

        keywords = feature_keywords.get(feature, [feature.lower(), feature.replace(" ", "")])

        # Search through source files
        src_dir = os.path.join(project_path, "frontend", "src")
        if not os.path.exists(src_dir):
            return False

        for root, _, files in os.walk(src_dir):
            for fname in files:
                if not fname.endswith((".js", ".jsx", ".ts", ".tsx", ".css")):
                    continue
                # Check filename
                for kw in keywords:
                    if kw.lower() in fname.lower():
                        return True

                # Check file content
                fpath = os.path.join(root, fname)
                try:
                    with open(fpath, "r", encoding="utf-8") as f:
                        content = f.read()
                    for kw in keywords:
                        if kw in content:
                            return True
                except (OSError, UnicodeDecodeError):
                    pass

        return False

    def _find_missing_features(self, project_path: str, spec: dict) -> list[str]:
        """Return list of required features that are not found in the codebase."""
        required = spec.get("required_features", [])
        return [f for f in required if not self._feature_exists(project_path, f)]

    # ── File Analysis Helpers ─────────────────────────────────────────────

    def _count_items(self, project_path: str, folder: str) -> int:
        """Count JS/JSX/TS/TSX files in a frontend/src subfolder."""
        target = os.path.join(project_path, "frontend", "src", folder)
        if not os.path.exists(target):
            return 0
        count = 0
        for _, _, files in os.walk(target):
            count += sum(1 for f in files if f.endswith((".js", ".jsx", ".ts", ".tsx")))
        return count

    def _find_placeholders(self, project_path: str) -> list[dict]:
        """Find files with placeholder/generic content."""
        results = []
        src_dir = os.path.join(project_path, "frontend", "src")
        if not os.path.exists(src_dir):
            return results

        for root, _, files in os.walk(src_dir):
            for fname in files:
                if not fname.endswith((".js", ".jsx", ".ts", ".tsx")):
                    continue
                fpath = os.path.join(root, fname)
                try:
                    with open(fpath, "r", encoding="utf-8") as f:
                        content = f.read()
                    for pattern in _PLACEHOLDER_PATTERNS:
                        match = re.search(pattern, content, re.IGNORECASE)
                        if match:
                            results.append({
                                "file": fname,
                                "detail": f"Contains placeholder: '{match.group()}'",
                            })
                            break
                except (OSError, UnicodeDecodeError):
                    pass

        return results

    def _find_branding_violations(self, project_path: str) -> list[dict]:
        """Find Agent OS branding in user-facing files (not in .agentos/)."""
        violations = []
        src_dir = os.path.join(project_path, "frontend", "src")
        readme_path = os.path.join(project_path, "README.md")

        # Check frontend source
        if os.path.exists(src_dir):
            for root, _, files in os.walk(src_dir):
                for fname in files:
                    fpath = os.path.join(root, fname)
                    try:
                        with open(fpath, "r", encoding="utf-8") as f:
                            content = f.read().lower()
                        for phrase in _BANNED_BRANDING:
                            if phrase in content:
                                violations.append({"file": fname, "text": phrase})
                                break
                    except (OSError, UnicodeDecodeError):
                        pass

        # Check README
        if os.path.exists(readme_path):
            try:
                with open(readme_path, "r", encoding="utf-8") as f:
                    content = f.read().lower()
                for phrase in _BANNED_BRANDING:
                    if phrase in content:
                        violations.append({"file": "README.md", "text": phrase})
                        break
            except (OSError, UnicodeDecodeError):
                pass

        return violations

    def _find_broken_imports(self, project_path: str) -> list[dict]:
        """Find broken relative imports in JS/JSX files."""
        broken = []
        src_dir = os.path.join(project_path, "frontend", "src")
        if not os.path.exists(src_dir):
            return broken

        for root, _, files in os.walk(src_dir):
            for fname in files:
                if not fname.endswith((".js", ".jsx", ".ts", ".tsx")):
                    continue
                fpath = os.path.join(root, fname)
                try:
                    with open(fpath, "r", encoding="utf-8") as f:
                        content = f.read()

                    imports = re.findall(r'import\s+.*?from\s+[\'\"](\.[\./][^\'\"]+)[\'"]', content)
                    for imp in imports:
                        dir_path = os.path.dirname(fpath)
                        target = os.path.normpath(os.path.join(dir_path, imp))
                        found = False
                        for ext in ["", ".js", ".jsx", ".ts", ".tsx", ".css"]:
                            if os.path.exists(target + ext):
                                found = True
                                break
                        if not found and os.path.isdir(target):
                            for ext in [".js", ".jsx", ".ts", ".tsx"]:
                                if os.path.exists(os.path.join(target, f"index{ext}")):
                                    found = True
                                    break
                        if not found:
                            broken.append({"file": fname, "import": imp})
                except (OSError, UnicodeDecodeError):
                    pass

        return broken


# ── Singleton ─────────────────────────────────────────────────────────────

evaluation_engine = EvaluationEngine()
